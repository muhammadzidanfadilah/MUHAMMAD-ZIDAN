#Bahasa pemrograman python

#Membuat a string
#Dengan single Quotes
pesan1 = 'cinta seorang ibu adalah sesuatu yang sangat berarti'
print("Tidak ada yang bisa menjelaskannya:")
print(pesan1)

#membuat String
#Dengan double Quotes
pesan2 = "cinta seorang ibu itu terbuat dari pengabdian yang mendalam"
print("\nTidak akan pernah ada habisnya sampai kapanpun:")
print(pesan2)

#membuat string
#Dengan tiga Quotes
pesan3 = '''karena tidak ada yang bisa'''
print("\nMenghancurkan atau mengambil cinta itu:")
print(pesan3)

#Creating String With Triple
#Quotes allows multiple lines
pesan4 = '''dan cinta ibu bersinar dengan segala keindahannya'''
print("\nDari permata yang paling langka dan paling cemerlang:")
print(pesan4)